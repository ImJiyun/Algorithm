SELECT
    e1.ID,
    COUNT(e2.ID) AS CHILD_COUNT
FROM 
    ECOLI_DATA AS e1
LEFT JOIN 
    ECOLI_DATA AS e2
ON
    e1.ID = e2.PARENT_ID
GROUP BY
    e1.ID
